<template>
	<div class="toast" v-show="isShow">
		<div>{{message}}</div>
	</div>
</template>

<script>
	export default{
		name:'Toast',
		props:{
			// message:{
			// 	type:String,
			// 	default:''
			// },
			// show:{
			// 	type:Boolean,
			// 	default:true
			// }
		},
		 data(){
		       return {
		           message:'',
		           isShow:false
		       }
		},
		methods:{
			show(message='默认文字',duration=1000){
				//console.log('999999')
				this.isShow=true;
				this.message=message;
				//console.log(message,duration)
				setTimeout(()=>{
				this.isShow=false;
				this.message='';	
				},duration);
			}
		}
		
	}
</script>

